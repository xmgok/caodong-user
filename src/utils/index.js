export const getParams = (url, name) => {
  url = url.substring(url.indexOf('?') + 1)
  const r = url.match(new RegExp('(^|&)' + name + '=([^&]*)(&|$)', 'i'))
  return r !== null ? unescape(r[2]) : null
}
